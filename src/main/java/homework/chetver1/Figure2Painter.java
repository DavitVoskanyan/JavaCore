package homework.chetver1;

public class Figure2Painter {
    public static void main(String[] args) {

      for (int k = 0; k < 5; k++) {
            for (int j = k; j < 5; j++) {
                System.out.print("  ");
            }
            for (int a = 0; a <= k; a++) {
                System.out.print("* ");
            }
            System.out.println();
        }

    }
}
















