package classwork.lesson8;

public class Ternary {
    public static void main(String[] args) {
        int i = 10;
        int k = i < 0 ? -i : i;
        System.out.print("Абсолиютное значение");
        System.out.println(i + "равно" + k);
        i = -10;
        k = i < 0 ? -i : i;
        System.out.print("Абсалютное значение");
        System.out.print("Абсалютное значение");
        System.out.println(i + "равно" + k);

    }
}
